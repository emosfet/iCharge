public class PileInfo {

    int fastPile =0;
    int slowPile =0;
    int freeFast =0;
    int freeSlow =0;

    
    public int getFastPile() {
        return fastPile;
    }

    public void setFastPile(int fastPile) {
        this.fastPile = fastPile;
    }

    public int getSlowPile() {
        return slowPile;
    }

    public void setSlowPile(int slowPile) {
        this.slowPile = slowPile;
    }

    public int getFreeFast() {
        return freeFast;
    }

    public void setFreeFast(int freeFast) {
        this.freeFast = freeFast;
    }

    public int getFreeSlow() {
        return freeSlow;
    }

    public void setFreeSlow(int freeSlow) {
        this.freeSlow = freeSlow;
    }



}
